/* bases.c -- выводит чмсло 100 в десятеричной, восьмиричной и шестнадцатиричной форме */
#include <stdio.h>
int main(void)
{
    int x = 100;
    printf("десятеричное = %d; восьмеричное = %o; шестнадцатиричное = %x\n", x, x, x);
    printf("десятеричное = %d; восьмеричное = %#o; шестнадцатиричное = %#x\n", x, x, x);

    return 0;
}